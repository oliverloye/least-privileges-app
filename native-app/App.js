import React from 'react';
import { Text, View, Platform, TouchableOpacity, StyleSheet, Button, WebView } from 'react-native';
import { Constants, WebBrowser } from "expo";
import { createStackNavigator  } from 'react-navigation';

import PersonTable from "./PersonTable";
//import facade from "./apiFacade";





class Movies extends React.Component {
  static navigationOptions = { title: "Learn about Props" }
  render() {
    return (
      <View>
       <View><PersonTable /></View>
      </View>
     
    )
  }
}

const Touchable = (props) => (
  <TouchableOpacity style={styles.button} onPress={props.onPress}>
    <Text style={styles.buttonText}>{props.title}</Text>
  </TouchableOpacity>)



class HomeScreen extends React.Component {
  static navigationOptions = { title: 'Day1 Tutorial' };
  render() {
    const { navigate } = this.props.navigation;
    return (
      <View >
        <Text style={{ textAlign: "center", fontSize: 20 }}>STAR WARS !!!!!!!!!!!</Text>
        
        <Touchable onPress={() => navigate('persons')} title="Persons" />
      </View>
    )
  }
}

export default App = () => <RouteStack style={{ marginTop: Platform.OS === 'ios' ? 0 : Constants.statusBarHeight / 2 }} />

const RouteStack = createStackNavigator({
  Home: { screen: HomeScreen },
  
  persons: { screen: Movies }
  
});

